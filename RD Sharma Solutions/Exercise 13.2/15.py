# By selling a pair of earrings at a discount of 25% on the marked price, a jeweller makes a profit of 16%. If the profit is Rs. 48, what is the cost price? What is the marked price and the price at which the pair was eventually bought?

# Solution

# Step 1: Define the profit and discount
profit = 48
discount_percent = 25
profit_percent = 16

# Step 2: Calculate the cost price
cost_price = 100 * profit / profit_percent
selling_price = cost_price + profit
marked_price = 100 * selling_price / (100 - discount_percent)

# Step 3: Print the result
print("Marked price = Rs", marked_price)
