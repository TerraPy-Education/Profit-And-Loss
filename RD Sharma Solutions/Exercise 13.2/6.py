# On the eve of Gandhi Jayanti a saree is sold for Rs. 720 after allowing 20% discount. What is its marked price?

# Solution

# Step 1: Define the selling price and discount
selling_price = 720
discount = 20

# Step 2: Calculate the marked price
marked_price = (100 * selling_price) / (100 - discount)

# Step 3: Print the result
print("M.P = Rs", marked_price)
