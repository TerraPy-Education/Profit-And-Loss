# The marked price of a ceiling fan is Rs 720. During off season, it is sold for Rs. 684. Determine the discount percent.

# Solution

# Step 1: Define the marked price and selling price
marked_price = 720
selling_price = 684

# Step 2: Calculate the discount
discount = (marked_price - selling_price) * 100 / marked_price

# Step 3: Print the result
print("Discount = ", discount, "%")
