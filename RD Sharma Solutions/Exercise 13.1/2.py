# Rekha bought a saree for Rs 1240 and sold it for Rs 1147. Find her loss and loss percent.

# Solution

# Step 1: Define the cost price and selling price of the saree
cost_price = 1240
selling_price = 1147

# Step 2: Calculate the loss
loss = cost_price - selling_price

# Step 3: Calculate the loss percent
loss_percent = (loss / cost_price) * 100

# Rounding off the loss percent to 2 decimal places
loss_percent = round(loss_percent, 2)

# Step 4: Print the result
print("Loss = Rs", loss)
print("Loss percent = ", loss_percent, "%")