# If the selling price of 10 pens is equal to cost price of 14 pens, find the gain percent.

# Solution

# Step 1: Define the cost price and selling price of one pen
cost_price = 1 # Let the cost price of one pen be Rs 1
# CP of 14 pens = 14
# SP of 10 pens = 14
selling_price = 14/10 # SP of one pen = 14/10

# Step 2: Calculate the gain
gain = selling_price - cost_price
gain_percent = (gain / cost_price) * 100
gain_percent = round(gain_percent, 2)

# Step 3: Print the result
print("Gain percent = ", gain_percent, "%")