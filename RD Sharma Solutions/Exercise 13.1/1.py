# A student buys a pen for Rs 90 and sells it for Rs 100. Find his gain and gain percent.

# Solution

# Step 1: Define the cost price and selling price of the pen
cost_price = 90
selling_price = 100

# Step 2: Calculate the gain
gain = selling_price - cost_price

# Step 3: Calculate the gain percent
gain_percent = (gain / cost_price) * 100

# Rounding off the gain percent to 2 decimal places
gain_percent = round(gain_percent, 2)

# Step 4: Print the result
print("Gain = Rs", gain)
print("Gain percent = ", gain_percent, "%")