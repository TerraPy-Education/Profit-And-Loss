# If the selling price of 18 oranges is equal to the cost price of 16 oranges, find the loss percent.

# Solution

# Step 1: Define the cost price and selling price of one orange
cost_price = 1 # Let the cost price of one orange be Rs 1
# CP of 16 oranges = 16
# SP of 18 oranges = 16
selling_price = 16/18 # SP of one orange = 16/18

# Step 2: Calculate the loss
loss = cost_price - selling_price
loss_percent = (loss / cost_price) * 100
loss_percent = round(loss_percent, 2)

# Step 3: Print the result
print("Loss percent = ", loss_percent, "%")
