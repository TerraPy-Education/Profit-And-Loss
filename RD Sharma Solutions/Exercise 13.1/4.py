# The cost price of 10 articles is equal to the selling price of 9 articles. Find the profit percent.

# Solution

# Step 1: Define the cost price and selling price of one article
cost_price = 1 # Let the cost price of one article be Rs 1
# CP of 10 articles = 10
# SP of 9 articles = 10
selling_price = 10/9 # SP of one article = 10/9

# Step 2: Calculate the profit
profit = selling_price - cost_price
profit_percent = (profit / cost_price) * 100
profit_percent = round(profit_percent, 2)

# Step 3: Print the result
print("Profit percent = ", profit_percent, "%")