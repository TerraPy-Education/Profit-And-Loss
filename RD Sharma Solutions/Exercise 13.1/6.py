# A retailer buys a cooler for Rs 1200 and overhead expenses on it are Rs 40. If he sells the cooler for Rs 1550, determine his profit percent.

# Solution

# Step 1: Define the cost price and selling price of the cooler
cost_price = 1200 + 40 # Overhead expenses add up to CP
selling_price = 1550

# Step 2: Calculate the profit
profit = selling_price - cost_price

# Step 3: Calculate the profit percent
profit_percent = (profit / cost_price) * 100
profit_percent = round(profit_percent, 2)

# Step 4: Print the result
print("Profit = Rs", profit)
print("Profit percent = ", profit_percent, "%")
