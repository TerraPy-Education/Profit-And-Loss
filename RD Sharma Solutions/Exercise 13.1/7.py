# A dealer buys a wristwatch for Rs 225 and spends Rs 15 on its repairs. If he sells the same for Rs 300, find his profit percent.

# Solution

# Step 1: Define the cost price and selling price of the wristwatch
cost_price = 225 + 15 # Repairs add up to CP
selling_price = 300

# Step 2: Calculate the profit
profit = selling_price - cost_price

# Step 3: Calculate the profit percent
profit_percent = (profit / cost_price) * 100
profit_percent = round(profit_percent, 2)

# Step 4: Print the result
print("Profit = Rs", profit)
print("Profit percent = ", profit_percent, "%")
